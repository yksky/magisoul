package org.magisoul.gcode.model;

public class ItemVo {
	
	//关键字名称
	private String itemFieldName ;
	//关键字值名称
	private String itemValueName ;
	
	public String getItemFieldName() {
		return itemFieldName;
	}
	public void setItemFieldName(String itemFieldName) {
		this.itemFieldName = itemFieldName;
	}
	public String getItemValueName() {
		return itemValueName;
	}
	public void setItemValueName(String itemValueName) {
		this.itemValueName = itemValueName;
	}
	
}
